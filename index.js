// Sum 2 (two) numbers
function sum(a, b) {
    return a+b;
}

// Subtract 2 (two) numbers
function subtraction(a, b) {
    return a-b;
}

// Divide 2 (two) numbers
function division(a, b) {
    return a/b;
}

// Multiplicate 2 (two) numbers
function multiplication(a, b) {
    return a*b;
}

// Floor a number
function floor(n) {
    return Math.floor(n);
}

// Ceil a number
function ceil(n) {
    return Math.ceil(n);
}
